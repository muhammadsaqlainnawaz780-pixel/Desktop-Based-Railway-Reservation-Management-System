    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        
         String passengerID = Pno.getText().trim();

    // Validate Passenger ID
    if (passengerID.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter Passenger ID to cancel reservation.");
        Pno.requestFocus();
        return;
    }

    // Confirm cancellation
    int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to cancel this reservation?", "Confirm", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.NO_OPTION) {
        return;
    }

    // Attempt to delete from database
    try {
        // Connect to MySQL
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/railway", "root", "Saq@22shergarh");

        // Prepare DELETE query
        PreparedStatement pst = con.prepareStatement("DELETE FROM reservation WHERE pno = ?");
        pst.setString(1, passengerID);

        // Execute deletion
        int rows = pst.executeUpdate();

        if (rows > 0) {
            JOptionPane.showMessageDialog(this, "Reservation cancelled successfully.");
        } else {
            JOptionPane.showMessageDialog(this, "No reservation found with that Passenger ID.");
        }

    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error cancelling reservation: " + ex.getMessage());
    }
    
    
    
    
    
    }//GEN-LAST:event_jButton4ActionPerformed

