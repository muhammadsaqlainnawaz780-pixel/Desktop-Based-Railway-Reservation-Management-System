    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         String pno=Pno.getText();
        String startplace=Splace.getSelectedItem().toString();
         String endplace=Eplace.getSelectedItem().toString();
         String tno=Tno.getText();
         String tname=Tname.getText();
          String Price=price.getText();
          SimpleDateFormat date_form = new SimpleDateFormat("yyyy-MM-dd");
          String Date= date_form.format(date.getDate()) ;
          String Ticketno=ticket.getText();
          String Total=total.getText();
          
          // ---- Check for empty fields ----
        if (pno.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Passanger Number is Necessary Required. Please Enter...");
        return;
}
          
          
          // Validation For Train Number
          if (!pno.matches("\\d+")) {
          JOptionPane.showMessageDialog(this, "Passanger Number must contain digits only.");
          Pno.requestFocus();
          return;
          }
          
          
        try {
            pst=con.prepareStatement("insert into reservation(pno,splace,eplace,tno,tname,price,date,ticket,tot)values(?,?,?,?,?,?,?,?,?)");
            
            pst.setString(1,pno );
            pst.setString(2,startplace );
            pst.setString(3,endplace);
            pst.setString(4,tno );
            pst.setString(5,tname );
            pst.setString(6, Price);
            pst.setString(7,Date );
            pst.setString(8,Ticketno );
            pst.setString(9,Total);
            
            int k=pst.executeUpdate();
            if (k==1) {
                JOptionPane.showMessageDialog(this, "Reserve Successfully");
            }else{
            
             JOptionPane.showMessageDialog(this, "Reserve Failed");
            
            }
           
        } catch (SQLException ex) {
            Logger.getLogger(AddTrain.class.getName()).log(Level.SEVERE, null, ex);
        }
         
    }//GEN-LAST:event_jButton1ActionPerformed

